# Определение функции func с параметрами x, y, z
def func(x, y, z):
    # Возвращаем результат вычисления булевого выражения
    # Выражение вычисляется следующим образом:
    # 1 Выполняется операция "И" (and) между y и z
    # 2 Результат операции "И" суммируется с x по модулю 2 (исключающее ИЛИ - XOR)
    # 3 Результат сравнивается с y при помощи оператора "==", который возвращает True (1), если оба операнда равны
    return (x ^ (y + z)) == y

