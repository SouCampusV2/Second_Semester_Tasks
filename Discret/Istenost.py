# Вложенные циклы для проверки всех комбинаций значений x, y и z
for x in [False, True]:
    for y in [False, True]:
        for z in [False, True]:
            # Вычисление результата для текущих значений x, y и z
            result = func(x, y, z)
            # Вывод значений x, y, z и соответствующего результата
            print(f"{int(x)}\t{int(y)}\t{int(z)}\t{int(result)}")
